# Context switch back to Thread A
result_a = value_a + 1
setattr(counter, 'count', result_a)


# Example 7
from threading import Lock

class LockingCounter:
    def __init__(self):
        self.lock = Lock()
        self.count = 0

    def increment(self, offset):
        with self.lock:
            self.count += offset


